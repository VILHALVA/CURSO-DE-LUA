-- Exemplo 1: Comparação de números
local numero1 = 10
local numero2 = 5

if numero1 == numero2 then
    print("Os números são iguais.")
else
    print("Os números são diferentes.")
end

-- Exemplo 2: Comparação de strings
local nome1 = "Alice"
local nome2 = "Bob"

if nome1 == nome2 then
    print("Os nomes são iguais.")
else
    print("Os nomes são diferentes.")
end

-- Exemplo 3: Comparação de valores booleanos
local valor1 = true
local valor2 = false

if valor1 == valor2 then
    print("Os valores booleanos são iguais.")
else
    print("Os valores booleanos são diferentes.")
end
