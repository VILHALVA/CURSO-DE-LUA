-- Exemplo de operador "igual a" (==)
local a = 5
local b = 5

if a == b then
    print("a é igual a b")
else
    print("a não é igual a b")
end

-- Exemplo de operador "diferente de" (~=)
local x = 10
local y = 20

if x ~= y then
    print("x é diferente de y")
else
    print("x não é diferente de y")
end

-- Exemplo de operador "maior que" (>)
local idade = 18

if idade > 21 then
    print("Pode beber bebidas alcoólicas nos EUA")
else
    print("Não pode beber bebidas alcoólicas nos EUA")
end

-- Exemplo de operador "menor que" (<)
local nota = 7

if nota < 5 then
    print("Reprovado")
else
    print("Aprovado")
end

-- Exemplo de operador "maior ou igual a" (>=)
local saldo = 1000
local limite = 1000

if saldo >= limite then
    print("Saldo é igual ou maior que o limite")
else
    print("Saldo é menor que o limite")
end
