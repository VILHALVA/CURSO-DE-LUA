-- Exemplo de operador de adição (+)
local resultadoAdicao = 5 + 3
print("Adição:", resultadoAdicao)  -- Isso imprimirá "Adição: 8"

-- Exemplo de operador de subtração (-)
local resultadoSubtracao = 10 - 4
print("Subtração:", resultadoSubtracao)  -- Isso imprimirá "Subtração: 6"

-- Exemplo de operador de multiplicação (*)
local resultadoMultiplicacao = 6 * 7
print("Multiplicação:", resultadoMultiplicacao)  -- Isso imprimirá "Multiplicação: 42"

-- Exemplo de operador de divisão (/)
local resultadoDivisao = 20 / 4
print("Divisão:", resultadoDivisao)  -- Isso imprimirá "Divisão: 5"

-- Exemplo de operador de resto (%)
local resultadoResto = 15 % 4
print("Resto:", resultadoResto)  -- Isso imprimirá "Resto: 3"
